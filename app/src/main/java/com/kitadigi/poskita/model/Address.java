package com.kitadigi.poskita.model;

public class Address {
    private String idAddress, userAddress, nameAddress, usersPhone;

    public Address(){}

    public String getIdAddress() { return idAddress; }

    public void setIdAddress(String idAddress) { this.idAddress = idAddress; }

    public String getUserAddress() { return userAddress; }

    public void setUserAddress(String userAddress) { this.userAddress = userAddress; }

    public String getNameAddress() { return nameAddress; }

    public void setNameAddress(String nameAddress) { this.nameAddress = nameAddress; }

    public String getUsersPhone() { return usersPhone; }

    public void setUsersPhone(String usersPhone) { this.usersPhone = usersPhone; }
}
