package com.kitadigi.poskita.model;

public class TransactionsDetail {
    String no, idItems, itemsName, qty, itemsPrice, subTotal;

    public TransactionsDetail(){}

    public String getNo() { return no; }

    public void setNo(String no) { this.no = no; }

    public String getIdItems() { return idItems; }

    public void setIdItems(String idItems) { this.idItems = idItems; }

    public String getItemsName() { return itemsName; }

    public void setItemsName(String itemsName) { this.itemsName = itemsName; }

    public String getQty() { return qty; }

    public void setQty(String qty) { this.qty = qty; }

    public String getItemsPrice() { return itemsPrice; }

    public void setItemsPrice(String itemsPrice) { this.itemsPrice = itemsPrice; }

    public String getSubTotal() { return subTotal; }

    public void setSubTotal(String subTotal) { this.subTotal = subTotal; }

}
