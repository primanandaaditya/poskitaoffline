package com.kitadigi.poskita.model;

public class Items {
    private String idItems, itemsName, itemsPrice, itemsPriceSell, itemsImage, itemsDescription, tipe, shortcut;

    public Items(){}

    public String getIdItems() { return idItems; }

    public void setIdItems(String idItems) { this.idItems = idItems; }

    public String getItemsName() { return itemsName; }

    public void setItemsName(String itemsName) { this.itemsName = itemsName; }

    public String getItemsPrice() { return itemsPrice; }

    public void setItemsPrice(String itemsPrice) { this.itemsPrice = itemsPrice; }

    public String getItemsPriceSell() { return itemsPriceSell; }

    public void setItemsPriceSell(String itemsPriceSell) { this.itemsPriceSell = itemsPriceSell; }

    public String getItemsImage() { return itemsImage; }

    public void setItemsImage(String itemsImage) { this.itemsImage = itemsImage; }

    public String getItemsDescription() { return itemsDescription; }

    public void setItemsDescription(String itemsDescription) { this.itemsDescription = itemsDescription; }

    public String getTipe() { return tipe; }

    public void setTipe(String tipe) { this.tipe = tipe; }

    public String getShortcut() { return shortcut; }

    public void setShortcut(String shortcut) { this.shortcut = shortcut; }
}
